package com.qintess.tools;

public class DadosActor {

	private String nome;
	private String sobrenome;
	
	
	public DadosActor(String nome, String sobrenome) {
		super();
		this.nome = nome;
		this.sobrenome = sobrenome;
	}


	public String getNome() {
		return nome;
	}


	public String getSobrenome() {
		return sobrenome;
	}
	
	
}
